// 54. Write a program to check whether a number is even or odd.
public class EvenOrOdd {
    public static void main(String[] args) {
        int num = 7; // Change num to check for different numbers
        if (num % 2 == 0) {
            System.out.println(num + " is even.");
        } else {
            System.out.println(num + " is odd.");
        }
    }
}
